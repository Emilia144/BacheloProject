class LanguageManager {
  constructor() {
    this.currentLang = localStorage.getItem('language') || 'de';
    this.initialized = false;
  }
  
  init() {
    if (this.initialized) return;
    this.initialized = true;
    
    // Set initial language
    this.applyLanguage(this.currentLang);
    
    // Setup language toggle button
    const toggleBtn = document.getElementById('language-toggle');
    if (toggleBtn) {
      toggleBtn.addEventListener('click', () => this.toggleLanguage());
    }
    
    // Update HTML lang attribute
    document.documentElement.lang = this.currentLang;
  }
  
  toggleLanguage() {
    this.currentLang = this.currentLang === 'de' ? 'en' : 'de';
    localStorage.setItem('language', this.currentLang);
    this.applyLanguage(this.currentLang);
    document.documentElement.lang = this.currentLang;
  }
  
  applyLanguage(lang) {
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
      const key = element.getAttribute('data-i18n');
      const translation = this.getTranslation(key, lang);
      
      if (translation) {
        // Check if element has specific attribute to update
        const attr = element.getAttribute('data-i18n-attr');
        if (attr) {
          element.setAttribute(attr, translation);
        } else if (element.getAttribute('data-i18n-html') === 'true') {
          element.innerHTML = translation;
        } else {
          element.textContent = translation;
        }
      }
    });

    // Notify page scripts that language has changed
    document.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));

    // Update language label in toggle button
    const langLabel = document.getElementById('language-label');
    if (langLabel) {
      if (lang === 'de') {
        langLabel.textContent = 'DE';
      } else {
        langLabel.textContent = 'EN';
      }
    }
  }
  
  getTranslation(key, lang) {
    return translations[lang]?.[key] || translations['de'][key] || key;
  }
  
  getCurrentLanguage() {
    return this.currentLang;
  }
}

// Initialize language manager when DOM and header are ready
function initLanguageManager() {
  if (!window.languageManager) {
    window.languageManager = new LanguageManager();
  }
  window.languageManager.init();
}

// Wait for header to be loaded (dynamically)
window.addEventListener('headerLoaded', initLanguageManager);

// Fallback: Initialize after a short delay if headerLoaded event hasn't fired
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', () => {
    setTimeout(() => {
      if (!window.languageManager || !window.languageManager.initialized) {
        initLanguageManager();
      }
    }, 100);
  });
} else {
  setTimeout(() => {
    if (!window.languageManager || !window.languageManager.initialized) {
      initLanguageManager();
    }
  }, 100);
}
